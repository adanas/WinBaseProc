INT_PTR ClsBaseModalDlg::dialog_box(LPCTSTR lpTempleteName, HWND hwndParent)
{
	if (m_hWnd)
		return -1;

	return DialogBoxParam(get_hInstance(), lpTempleteName, hwndParent,
		(DLGPROC) call_dlg_proc, (LPARAM) this);
} // End Of Fnc

